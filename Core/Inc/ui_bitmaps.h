/* ═══════════════════════════════════════════════════════════════════════════
   Bitmap descriptor — pass this to UI_DrawBitmap()
   ═══════════════════════════════════════════════════════════════════════════ */
typedef struct {
    const uint8_t *data;    /* pointer to the bmp_xxx[] array               */
    uint8_t        w;       /* pixel width                                   */
    uint8_t        h;       /* pixel height                                  */
    uint8_t        bpr;     /* bytes per row                                 */
} UI_Bitmap_t;

