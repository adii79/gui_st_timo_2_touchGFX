/**
 ******************************************************************************
 * @file    ui_bitmaps.h
 * @brief   1-bit icon assets in row-major binary literals.
 *
 * Format rules:
 *   - Row-major: bmp[row * BPR + byte_col]
 *   - MSB of each byte = leftmost pixel in that group of 8
 *   - '1' bit = foreground (fg colour at draw time)
 *   - '0' bit = transparent / background
 *   - BPR = bytes per row = ceil(width / 8)
 *
 * Each icon ships as:
 *   BMP_<NAME>_W   pixel width
 *   BMP_<NAME>_H   pixel height
 *   BMP_<NAME>_BPR bytes per row
 *   bmp_<name>[]   data array
 *
 * Reading the data: each 0b literal IS the pixel row.
 *   0b00001111u, 0b11000000u  →  ....######......
 *   First byte: cols 0-7, second byte: cols 8-15 (MSB=left always).
 ******************************************************************************
 */

